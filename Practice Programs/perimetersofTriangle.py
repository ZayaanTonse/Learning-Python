#Program to calculate the perimeters of a triangle

side1=float(input("Enter the first side of the triangle(in units)"))
side2=float(input("Enter the second side of the triangle(in units)"))
side3=float(input("Enter the third side of the triangle(in units)"))

perimeter=side1+side2+side3

print("The perimeter of the triangle is:",perimeter,"units")